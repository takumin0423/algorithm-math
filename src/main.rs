fn main() {
    // 四則演算
    println!("{}", 869 + 120);
    println!("{}", 869 - 120);
    println!("{}", 869 * 120);
    println!("{}", 869 / 120);

    // 剰余
    println!("{}", 869 % 120);

    // 絶対値
    println!("{}", (-45i32).abs());
    println!("{}", (20i32).abs());

    // 累乗
    println!("{}", (20i32).pow(2));

    // ルート
    println!("{}", (20.0f64).sqrt());
}
